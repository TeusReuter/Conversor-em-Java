package model.bean;

/**
 *
 * @author Aluno
 */
public class Operation {
    
    public int idOperacao;
    public String codigoProduto;
    public String descOperacao;
    public int quantProduto;
    public String dataOperacao;

    public int getIdOperacao() {
        return idOperacao;
    }

    public void setIdOperacao(int idOperacao) {
        this.idOperacao = idOperacao;
    }

    public String getCodigoProduto() {
        return codigoProduto;
    }

    public void setCodigoProduto(String codigoProduto) {
        this.codigoProduto = codigoProduto;
    }

    public String getDescOperacao() {
        return descOperacao;
    }

    public void setDescOperacao(String descOperacao) {
        this.descOperacao = descOperacao;
    }

    public int getQuantProduto() {
        return quantProduto;
    }

    public void setQuantProduto(int quantProduto) {
        this.quantProduto = quantProduto;
    }

    public String getDataOperacao() {
        return dataOperacao;
    }

    public void setDataOperacao(String dataOperacao) {
        this.dataOperacao = dataOperacao;
    }
    
}
